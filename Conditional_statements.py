#Examples of conditional statements

#if statement:-
num = 10
if num > 0:
    print("Positive number")
#output - Positive number

#if-else statement:-
num = -5
if num > 0:
    print("Positive number")
else:
    print("Negative number")
#output - Negative number

#if-elif-else statement:-
num = 0
if num > 0:
    print("Positive number")
elif num == 0:
    print("Zero")
else:
    print("Negative number")
#output - Zero
